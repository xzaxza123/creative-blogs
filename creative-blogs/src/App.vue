<template>
<div>
    <router-link to="/"></router-link> 
    <router-view v-if="isRouterAlive"></router-view>
</div>
  
</template>

<script>
export default {
  data() {
    return {
      isRouterAlive: true,
    };
  },
  provide() {
    //提供
    return {
      reload: this.reload,
    };
  },
  //局部刷新
  methods: {
    reload() {
      this.isRouterAlive = false;
      this.$nextTick(function () {
        this.isRouterAlive = true;
      });
    },
  },
};
</script>


